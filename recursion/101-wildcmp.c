#include "main.h"

/**
 * skip_stars - рекурсивно пропускает подряд идущие '*'
 * @s2: строка шаблона
 * Return: указатель на первый символ после серии '*'
 */
char *skip_stars(char *s2)
{
    if (*s2 == '*' && *(s2 + 1) == '*')
        return skip_stars(s2 + 1);
    return s2;
}

/**
 * wildcmp - сравнивает строки с поддержкой '*' в s2
 * @s1: первая строка
 * @s2: вторая строка, может содержать '*'
 * Return: 1 если совпадают, 0 если нет
 */
int wildcmp(char *s1, char *s2)
{
    if (*s1 == '\0' && *s2 == '\0') // обе строки закончились
        return 1;

    if (*s2 == '*')
    {
        s2 = skip_stars(s2); // убираем повторяющиеся '*'

        // '*' соответствует пустой строке, или соответствует первому символу s1 и далее
        if (wildcmp(s1, s2 + 1))
            return 1;
        if (*s1 != '\0' && wildcmp(s1 + 1, s2))
            return 1;

        return 0;
    }

    if (*s1 == *s2)
        return wildcmp(s1 + 1, s2 + 1);

    return 0;
}
